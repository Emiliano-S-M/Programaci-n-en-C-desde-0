#include <stdio.h>

int main() {
    int edad = 20;
    
    if (edad >= 18) {
        printf("Eres mayor de edad.\n");
    }
    
    return 0;
}

/*

En este ejemplo, si la variable edad es mayor o igual a 18, se imprimirá "Eres mayor de edad.". 
De lo contrario, este mensaje no se imprimirá. La condición edad >= 18 se evalúa como verdadera 
si edad es 18 o mayor, y como falsa si edad es menor que 18.

*/